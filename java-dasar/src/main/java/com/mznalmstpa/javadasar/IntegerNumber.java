package com.mznalmstpa.javadasar;

public class IntegerNumber {
    public static void main(String[] args) {

        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 100000;
        long iniLong = 10000000;
        long iniLong2 = 1000000L;

        // floating point number
        float iniFloat = 10.12F;
        double iniDouble = 12.2424;

        // literals
        int decimalInt = 25;
        int hexInt = 0xA132B;
        int binInt = 0b010101;

        // underscore
        long balance = 1_000_000_000_000L;
        int sum = 60_000_000;
    }
}
