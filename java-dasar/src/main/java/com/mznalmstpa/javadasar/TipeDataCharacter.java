package com.mznalmstpa.javadasar;

public class TipeDataCharacter {

    public static void main(String[] args) {

        // tipe data character
        // huruf di java di representasikan oleh tipe char
        // untuk membuat data char di java , kita bisa menggunakan tanda ' (petik satu) di awal dan di akhir karakter

        char T = 'T';
        char O = 'O';
        char P = 'P';
        char A = 'A';

        System.out.println(T);
        System.out.println(O);
        System.out.println(P);
        System.out.println(A);
    }
}
